// Helpers para usar desde las p√°ginas

export function getHelpArticlesByCountry(countryCode) {
  const key = countryCode?.toLowerCase();
  const country = helpArticlesData[key];

  if (!country) return { country: null, articles: [] };

  return {
    country,
    articles: country.articles ?? [],
  };
}

export function findHelpArticle(countryCode, slug) {
  const { country, articles } = getHelpArticlesByCountry(countryCode);
  if (!country) return null;

  const article = articles.find((a) => a.slug === slug);
  if (!article) return null;

  return { country, article };
}

// Para getStaticPaths de [pais]/ayuda/[slug].astro
export function getAllHelpPaths() {
  const paths = [];

  for (const countryCode of Object.keys(helpArticlesData)) {
    const entry = helpArticlesData[countryCode];
    const articles = entry.articles ?? [];

    for (const article of articles) {
      paths.push({
        params: {
          pais: countryCode,
          slug: article.slug,
        },
        props: {
          countryCode,
          countryName: entry.country,
          flag: entry.flag,
          article,
        },
      });
    }
  }

  return paths;
}


